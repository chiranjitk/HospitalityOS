// Helper: simple common word/phrase translation for fallback
function simpleDe(english) {
  if (!english || typeof english !== 'string') return english;
  // Keep placeholders, URLs, emails, tech terms
  if (/^{.+}$/.test(english.trim()) || /^https?:\/\//.test(english) || /@/.test(english) && english.includes('.')) return english;
  return english; // fallback: keep original
}

