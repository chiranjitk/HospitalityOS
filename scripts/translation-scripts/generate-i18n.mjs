function getFiles(dir) {
  return fs.readdirSync(dir).filter(f => f.endsWith('.tsx')).map(f => path.join(dir, f));
}

function extractNamespaceFromPath(filePath) {
  for (const [ns, nsPath] of Object.entries(DIRS)) {
    if (filePath.startsWith(nsPath)) return ns;
  }
  return 'unknown';
}

