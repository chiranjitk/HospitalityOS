// Helper: recursively translate a flat key
function translateValue(enValue, locale, tMap) {
  const words = enValue.split(' ');
  return words.map(w => {
    const lower = w.toLowerCase().replace(/[^a-z0-9]/g, '');
    if (tMap[lower]) return tMap[lower];
    if (tMap[w]) return tMap[w];
    return w; // fallback to English for non-translatable words
  }).join(' ');
}

// Main: generate translations for each locale
const locales = ['en', 'ar', 'bn', 'de', 'es', 'fr', 'gu', 'hi', 'ja', 'ml', 'mr', 'pt', 'ta', 'te', 'zh'];

for (const locale of locales) {
  const localePath = path.join(MSG, `${locale}.json`);
  const existing = JSON.parse(fs.readFileSync(localePath, 'utf-8'));
  
  if (locale === 'en') {
    // Add all 4 new namespaces
    for (const [ns, keys] of Object.entries(allEN)) {
      existing[ns] = keys;
    }
  } else {
    // Translate each namespace
    for (const [ns, enKeys] of Object.entries(allEN)) {
      const tMap = TRANSLATIONS[locale]?.translations || TRANSLATIONS.en.translations;
      existing[ns] = {};
      for (const [key, enVal] of Object.entries(enKeys)) {
        existing[ns][key] = translateValue(enVal, locale, tMap);
      }
    }
  }
  
  fs.writeFileSync(localePath, JSON.stringify(existing, null, 2) + '\n', 'utf-8');
}

