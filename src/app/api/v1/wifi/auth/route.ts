function parseDeviceTypeFromUA(ua: string): string {
  if (/iPhone/i.test(ua)) return 'mobile';
  if (/iPad/i.test(ua) || (/Macintosh/i.test(ua) && /WebKit/i.test(ua) && !/Safari/i.test(ua))) return 'tablet';
  if (/Android/i.test(ua)) return /Mobile/i.test(ua) ? 'mobile' : 'tablet';
  if (/SmartTV|InternetTV|APPLETV/i.test(ua)) return 'tv';
  if (/Windows|Macintosh|Linux|CrOS/i.test(ua)) return 'desktop';
  return 'unknown';
}

function parseDeviceNameFromUA(ua: string): string {
  if (/iPhone/i.test(ua)) return 'iPhone';
  if (/iPad/i.test(ua)) return 'iPad';
  if (/Android/i.test(ua)) return /Mobile/i.test(ua) ? 'Android Phone' : 'Android Tablet';
  if (/Windows/i.test(ua)) return 'Windows PC';
  if (/Macintosh/i.test(ua)) return 'Mac';
  if (/CrOS/i.test(ua)) return 'Chromebook';
  if (/Linux/i.test(ua)) return 'Linux PC';
  if (/SmartTV/i.test(ua)) return 'Smart TV';
  return 'Unknown Device';
}

/**
 * Generate a synthetic fingerprint hash from a MAC address.
 * Used when the browser's crypto.subtle is unavailable (e.g. HTTP connections)
 * so that the DeviceProfile can still be created with MAC-based tracking.
 *
 * The prefix "syn-mac:" ensures these never collide with real browser fingerprints
 * generated by device-fingerprint.ts (which uses SHA-256 of Canvas/WebGL signals).
 */
function syntheticFingerprintFromMac(mac: string): string {
  const normalized = mac.replace(/[:\-\.\s]/g, '').toUpperCase();
  return createHash('sha256')
    .update(`syn-mac:${normalized}`)
    .digest('hex');
}

/**
 * Create or update a DeviceProfile entry.
 *
 * Priority:
 * 1. Real browser fingerprintHash (from device-fingerprint.ts) — preferred for auto-reauth
 * 2. MAC-based synthetic fingerprint — fallback when crypto.subtle unavailable (HTTP)
 * 3. No profile created if neither is available (deferred to auto-auth fire-and-forget)
 */
async function upsertDeviceProfileWithFingerprint(params: {
  wifiUserId: string;
  tenantId: string;
  propertyId: string;
  guestId?: string | null;
  username: string;
  request: NextRequest;
  macAddress?: string | null;
  fingerprintHash?: string | null;
  storageToken?: string | null;
}) {
  const { wifiUserId, tenantId, propertyId, guestId, username, request, macAddress, fingerprintHash, storageToken } = params;

  // Normalize MAC address format
  const normalizedMac = macAddress
    ? macAddress.replace(/[:\-\.\s]/g, '').toUpperCase()
    : null;
  const formattedMac = normalizedMac && normalizedMac.length === 12
    ? normalizedMac.match(/.{2}/g)?.join(':') || null
    : null;

  // Determine fingerprint: prefer real browser fingerprint, fall back to MAC-based synthetic
  let effectiveFingerprint = fingerprintHash;
  let isSynthetic = false;
  if (!effectiveFingerprint && formattedMac) {
    effectiveFingerprint = syntheticFingerprintFromMac(formattedMac);
    isSynthetic = true;
  }

  if (!effectiveFingerprint) {
    console.log(`[Auth] No client fingerprint or MAC address — skipping DeviceProfile creation (deferred to auto-auth) [fp=${fingerprintHash || 'null'} mac=${macAddress || 'null'}]`);
    // Still sync WiFiDevice registry even without fingerprint (Issue #3)
    if (formattedMac) {
      await upsertWiFiDeviceRegistry({
        tenantId, guestId, propertyId, macAddress: formattedMac,
        deviceName: parseDeviceNameFromUA(request.headers.get('user-agent') || ''),
        deviceType: parseDeviceTypeFromUA(request.headers.get('user-agent') || ''),
        ipAddress: getClientIp(request) || undefined,
        userAgent: request.headers.get('user-agent') || undefined,
      });
    }
    return;
  }

  // Issue #4: Check if admin has disabled auto-auth for this specific device.
  // If autoAuth=false in WiFiDevice registry, skip DeviceProfile creation.
  // The user can still manually authenticate — they just won't auto-authenticate next time.
  if (formattedMac) {
    const autoAuthDisabled = await isDeviceAutoAuthDisabled(tenantId, formattedMac);
    if (autoAuthDisabled) {
      console.log(`[Auth] Device ${formattedMac} has autoAuth=DISABLED — skipping DeviceProfile creation (manual auth still allowed)`);
      // Still sync WiFiDevice registry
      await upsertWiFiDeviceRegistry({
        tenantId, guestId, propertyId, macAddress: formattedMac,
        deviceName: parseDeviceNameFromUA(request.headers.get('user-agent') || ''),
        deviceType: parseDeviceTypeFromUA(request.headers.get('user-agent') || ''),
        ipAddress: getClientIp(request) || undefined,
        userAgent: request.headers.get('user-agent') || undefined,
      });
      return;
    }
  }

  console.log(`[Auth] DeviceProfile upsert for ${username}: fp=${effectiveFingerprint ? effectiveFingerprint.substring(0, 16) + '...' : 'none'} mac=${formattedMac || 'none'} synthetic=${isSynthetic}`);

  try {
    const clientIp = getClientIp(request) || 'unknown';
    const userAgent = request.headers.get('user-agent') || '';
    const deviceType = parseDeviceTypeFromUA(userAgent);
    const deviceName = parseDeviceNameFromUA(userAgent);

    // Upsert by (fingerprintHash, propertyId) unique constraint
    await db.deviceProfile.upsert({
      where: {
        fingerprintHash_propertyId: {
          fingerprintHash: effectiveFingerprint,
          propertyId,
        },
      },
      create: {
        tenantId,
        propertyId,
        wifiUserId,
        guestId: guestId || undefined,
        fingerprintHash: effectiveFingerprint,
        storageToken: storageToken || undefined,
        ipAddress: clientIp,
        userAgent: userAgent.substring(0, 500),
        macAddress: formattedMac || undefined,
        deviceType,
        deviceName,
        authCount: 1,
        lastAuthAt: new Date(),
        lastSeenAt: new Date(),
        isActive: true,
      },
      update: {
        // If upgrading from synthetic → real fingerprint, keep the real one
        ...(fingerprintHash && isSynthetic === false ? { fingerprintHash } : {}),
        wifiUserId,
        guestId: guestId || undefined,
        storageToken: storageToken || undefined,
        ipAddress: clientIp,
        userAgent: userAgent.substring(0, 500),
        ...(formattedMac ? { macAddress: formattedMac } : {}),
        deviceType,
        deviceName,
        isActive: true,
        authCount: { increment: 1 },
        lastAuthAt: new Date(),
        lastSeenAt: new Date(),
      },
    });

    if (isSynthetic) {
      console.log(`[Auth] DeviceProfile upserted for ${username} (synthetic-fingerprint from MAC: ${formattedMac})`);
    } else {
      console.log(`[Auth] DeviceProfile upserted for ${username} (fingerprint: ${fingerprintHash!.substring(0, 12)}...)`);
    }

    // Issue #3: Also sync WiFiDevice registry so admin dashboard stays populated
    if (formattedMac) {
      await upsertWiFiDeviceRegistry({
        tenantId, guestId, propertyId, macAddress: formattedMac,
        deviceName, deviceType,
        ipAddress: clientIp,
        userAgent: userAgent.substring(0, 500),
      });
    }
  } catch (err) {
    console.warn('[Auth] DeviceProfile upsert failed (non-critical):', err instanceof Error ? err.message : err);
  }
}

