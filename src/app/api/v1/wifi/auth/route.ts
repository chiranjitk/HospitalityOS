/**
 * Resolve allowed IP pool IDs for a user based on their plan binding.
 *
 * Priority:
 * 1. WiFiUser.ipPoolId — user-level override → restrict to that pool only
 * 2. WiFiPlan.ipPoolId — plan default → restrict to that pool only
 * 3. Plan exists but has NO pool → check ALL pools (any pool is OK)
 * 4. No plan at all → don't check, allow any IP
 *
 * Returns:
 *   - string[] — specific pool IDs the user is restricted to
 *   - 'ANY' — check all pools, reject if not found in any
 *   - null — no restriction at all (no plan)
 */
function resolveAllowedPoolIds(
  planIpPoolId?: string | null,
  userIpPoolId?: string | null,
  hasPlan?: boolean,
): string[] | 'ANY' | null {
  // User-level override takes priority over plan-level
  if (userIpPoolId) return [userIpPoolId];
  if (planIpPoolId) return [planIpPoolId];
  // Plan exists but has no pool → check all pools
  if (hasPlan) return 'ANY';
  // No plan at all → no restriction
  return null;
}

/**
 * Check if a user has reached their concurrent session limit.
 * Counts active (non-stopped) radacct sessions for the username.
 * Returns true if the limit is reached (should reject login).
 */
async function isSessionLimitReached(username: string, maxSessions: number): Promise<boolean> {
  if (maxSessions <= 0) return false; // 0 or negative = unlimited

  try {
    // Only count real active sessions — exclude interim-update audit rows
    // which have acctstoptime IS NULL but are NOT actual user sessions.
    // Also exclude any rows with acctterminatecause set (already closed).
    const result = await db.$queryRawUnsafe<Array<{ count: bigint }>>(`
      SELECT COUNT(*)::bigint as count
      FROM radacct
      WHERE username = $1
        AND acctstoptime IS NULL
        AND (acctstatus IS NULL OR acctstatus = '' OR acctstatus = 'start')
        AND acctterminatecause IS NULL
    `, username);

    const activeCount = Number(result[0]?.count ?? 0);
    return activeCount >= maxSessions;
  } catch (err) {
    console.error('[Session Limit] Failed to count active sessions:', err);
    return true; // Fail closed — deny access on error
  }
}

