/**
 * Resolve client MAC address using multiple methods (priority order):
 *
 * 1. Request body — Captive portal frontend passes MAC from URL query params
 *    (e.g., ?mac=AA:BB:CC:DD:EE:FF from UniFi/Mikrotik/Cisco redirect)
 * 2. HTTP headers — Some network equipment injects MAC as headers:
 *    - X-MAC-Address, Client-MAC, Calling-Station-Id
 *    - mac (Mikrotik, CoovaChilli), nas-mac (UniFi)
 * 3. DHCP lease table — Look up by client IP as last resort
 *    (only works if KEA/DHCP is running and has recorded the lease)
 */
async function resolveMacAddress(
  request: NextRequest,
  bodyMac?: string,
  clientIp?: string,
): Promise<string | null> {
  // 1. Body MAC (from URL params forwarded by captive portal frontend)
  if (bodyMac) {
    const normalized = bodyMac.replace(/[:\-\.\s]/g, '').toUpperCase();
    if (/^[0-9A-F]{12}$/.test(normalized)) {
      return normalized.match(/.{2}/g)?.join(':') || null;
    }
  }

  // 2. HTTP headers from network equipment
  const headerSources = [
    'x-mac-address',
    'client-mac',
    'mac',
    'nas-mac',
    'calling-station-id',
    'x-client-mac',
    'x-forwarded-mac',
  ];
  for (const header of headerSources) {
    const value = request.headers.get(header);
    if (value) {
      // Calling-Station-Id is often "AA:BB:CC:DD:EE:FF" or "AA-BB-CC-DD-EE-FF"
      // Some vendors prefix with SSID: "AA:BB:CC:DD:EE:FF-MyNetwork"
      const macMatch = value.match(/([0-9A-Fa-f]{2}[:\-\.]){5}[0-9A-Fa-f]{2}/);
      if (macMatch) {
        const normalized = macMatch[0].replace(/[:\-\.\s]/g, '').toUpperCase();
        if (/^[0-9A-F]{12}$/.test(normalized)) {
          return normalized.match(/.{2}/g)?.join(':') || null;
        }
      }
    }
  }

  // 3. DHCP lease lookup by client IP (last resort)
  if (clientIp && clientIp !== '0.0.0.0' && clientIp !== 'unknown') {
    try {
      const leaseResult = await db.$queryRawUnsafe<Array<{ "macAddress": string | null }>>(`
        SELECT "macAddress" FROM "DhcpLease"
        WHERE "ipAddress" = $1 AND state = 'active'
        ORDER BY "lastSeenAt" DESC
        LIMIT 1
      `, clientIp);

      if (leaseResult.length > 0 && leaseResult[0].macAddress) {
        const normalized = leaseResult[0].macAddress.replace(/[:\-\.\s]/g, '').toUpperCase();
        if (/^[0-9A-F]{12}$/.test(normalized)) {
          console.log(`[MAC] Resolved from DHCP lease: IP=${clientIp} → MAC=${normalized.match(/.{2}/g)?.join(':')}`);
          return normalized.match(/.{2}/g)?.join(':') || null;
        }
      }
    } catch (err) {
      // Non-critical — DHCP lookup failure should not block auth
      console.warn('[MAC] DHCP lease lookup failed:', err instanceof Error ? err.message : err);
    }
  }

  return null;
}

// ────────────────────────────────────────────────────────────
// Per-IP Rate Limiter (in-memory — use Redis for production)
// ────────────────────────────────────────────────────────────
const authAttempts = new Map<string, { count: number; resetAt: number }>();
function checkRateLimit(ip: string, maxAttempts: number = 10, windowMs: number = 60000): boolean {
  const now = Date.now();
  const entry = authAttempts.get(ip);
  if (!entry || now > entry.resetAt) {
    authAttempts.set(ip, { count: 1, resetAt: now + windowMs });
    return true;
  }
  entry.count++;
  return entry.count <= maxAttempts;
}

// ────────────────────────────────────────────────────────────
// POST /api/v1/wifi/auth — Guest WiFi authentication
//
// Validation order (changed per requirement):
//   1. Parse request body → extract username/method
//   2. Validate credentials (username, password, voucher, room, OTP)
//      → if invalid, log and return credential-specific error
//   3. Validate client IP against allocated IP pools
//      → if invalid, log and return IP error
//   4. Authenticate via FreeRADIUS
//   5. Log success + create accounting session
//
// This ensures auth logs show the CORRECT rejection reason:
//   Wrong password → "Rejected — invalid credentials"
//   Wrong network  → "Rejected — IP not in managed pool"
// ────────────────────────────────────────────────────────────
export async function POST(request: NextRequest) {
  try {
    // ════════════════════════════════════════════════════════════
    // STEP 0: Rate limit check (per-IP)
    // ════════════════════════════════════════════════════════════
    const clientRateLimitIp = extractClientIp(request) || 'unknown';
    if (!checkRateLimit(clientRateLimitIp)) {
      return errorResponse('RATE_LIMITED', 'Too many authentication attempts. Please try again later.', 429);
    }

    // ════════════════════════════════════════════════════════════
    // STEP 1: Parse request body — extract username & method first
    // ════════════════════════════════════════════════════════════
    const body = await request.json();
    const {
      method,
      portalSlug,
      voucherCode,
      roomNumber,
      lastName,
      username,
      password,
      phoneNumber,
      otpCode,
      guestInfo,
      marketingEmailConsent,
      marketingSmsConsent,
      macAddress,
      fingerprintHash,
      storageToken,
    } = body as {
      method?: string;
      portalSlug?: string;
      voucherCode?: string;
      roomNumber?: string;
      lastName?: string;
      username?: string;
      password?: string;
      phoneNumber?: string;
      otpCode?: string;
      guestInfo?: {
        firstName?: string;
        lastName?: string;
        email?: string;
        phone?: string;
        passport?: string;
        bookingId?: string;
      };
      marketingEmailConsent?: string;
      marketingSmsConsent?: string;
      macAddress?: string;
      fingerprintHash?: string;
      storageToken?: string;
    };

    // Normalize guestInfo — unified form sends it as a JSON string
    let normalizedGuestInfo: GuestInfoPayload | undefined;
    if (typeof guestInfo === 'string') {
      try {
        normalizedGuestInfo = JSON.parse(guestInfo);
      } catch { /* ignore malformed JSON */ }
    } else if (typeof guestInfo === 'object' && guestInfo !== null) {
      normalizedGuestInfo = guestInfo as GuestInfoPayload;
    }


    if (!method) {
      return errorResponse('MISSING_METHOD', 'Authentication method is required');
    }

    // ════════════════════════════════════════════════════════════
    // STEP 1b: Resolve MAC address from multiple sources
    // Priority: request body → HTTP headers → DHCP lease lookup
    // ════════════════════════════════════════════════════════════
    const clientIpForMac = extractClientIp(request) || getClientIpString(request);
    const resolvedMac = await resolveMacAddress(request, macAddress, clientIpForMac);
    // Use resolved MAC (from headers/DHCP) if body didn't provide one
    const effectiveMac = macAddress || resolvedMac || undefined;
    console.log(`[Auth] MAC resolution: body=${macAddress || 'none'} resolved=${resolvedMac || 'none'} effective=${effectiveMac || 'none'} (client: ${clientIpForMac})`);
    if (effectiveMac && resolvedMac && !macAddress) {
      console.log(`[MAC] Auto-resolved MAC for client: ${clientIpForMac} → ${resolvedMac} (source: header/dhcp)`);
    }

    const validMethods = [
      'voucher',
      'room_number',
      'pms_credentials',
      'sms_otp',
      'open_access',
    ];

    if (!validMethods.includes(method)) {
      return errorResponse(
        'INVALID_METHOD',
        `Invalid authentication method. Supported: ${validMethods.join(', ')}`
      );
    }

    // Fetch portal config for session timeout & bandwidth info
    const portal = portalSlug
      ? await db.captivePortal.findFirst({
          where: { slug: portalSlug, enabled: true },
        })
      : null;

    // Pre-resolve the plan's ipPoolId for pool-restricted validation.
    // Used by SMS OTP and Open Access where no existing user/plan is available.
    // Voucher and PMS Credentials resolve their own plan from user/voucher data.
    let portalPlanIpPoolId: string | null = null;
    if (portal?.propertyId) {
      const aaaConfig = await db.wiFiAAAConfig.findUnique({
        where: { propertyId: portal.propertyId },
        select: { defaultPlanId: true },
      });
      if (aaaConfig?.defaultPlanId) {
        const portalPlan = await db.wiFiPlan.findUnique({
          where: { id: aaaConfig.defaultPlanId },
          select: { ipPoolId: true },
        });
        portalPlanIpPoolId = portalPlan?.ipPoolId ?? null;
      }
    }

    // Portal defaults (sessionTimeout is stored in SECONDS in DB, convert to minutes for display)
    const portalSessionTimeoutMin = portal?.sessionTimeout
      ? Math.round(portal.sessionTimeout / 60)
      : 1440; // default 24h in minutes
    const bwDown = portal
      ? Math.round((portal.maxBandwidthDown || 5242880) / 1000000)
      : 5;
    const bwUp = portal
      ? Math.round((portal.maxBandwidthUp || 1048576) / 1000000)
      : 1;

    // ════════════════════════════════════════════════════════════
    // STEP 2: Validate credentials FIRST
    // Each method validates its own credentials here.
    // If invalid, we log and return immediately — NO IP check yet.
    // ════════════════════════════════════════════════════════════
    switch (method) {
      // ─── Voucher ──────────────────────────────────────────
      case 'voucher': {
        if (!voucherCode?.trim()) {
          return errorResponse('MISSING_VOUCHER', 'Please enter a voucher code');
        }

        const voucher = await db.wiFiVoucher.findUnique({
          where: { code: voucherCode.trim().toUpperCase() },
          include: { plan: true },
        });

        if (!voucher) {
          await logAuthAttempt(voucherCode.trim(), 'Access-Reject', request, 'INVALID_VOUCHER');
          return errorResponse('INVALID_VOUCHER', 'Invalid or expired voucher code');
        }

        if (voucher.status !== 'active' || voucher.isUsed) {
          await logAuthAttempt(`voucher-${voucher.code.toLowerCase()}`, 'Access-Reject', request, 'VOUCHER_USED');
          return errorResponse('VOUCHER_USED', 'This voucher has already been used');
        }

        const now = new Date();
        if (voucher.validUntil < now) {
          await logAuthAttempt(`voucher-${voucher.code.toLowerCase()}`, 'Access-Reject', request, 'VOUCHER_EXPIRED');
          return errorResponse('VOUCHER_EXPIRED', 'This voucher has expired. Please contact front desk for a new one.');
        }

        // ── Credentials valid — now check IP pool ──
        const allowedPools = resolveAllowedPoolIds(voucher.plan?.ipPoolId, undefined, !!voucher.plan);
        const pool = await getValidatedPool(request, allowedPools);
        if (!pool) {
          await logAuthAttempt(`voucher-${voucher.code.toLowerCase()}`, 'Access-Reject', request, `IP_NOT_IN_POOL:${getClientIpString(request)}`);
          return errorResponse('IP_NOT_IN_POOL', 'Your device is not connected to a managed WiFi network. Please connect to the hotel WiFi and try again.', 403);
        }

        // Resolve propertyId
        const resolvedPropertyId = voucher.plan?.propertyId
          || await db.property.findFirst({ where: { tenantId: voucher.tenantId }, select: { id: true } }).then(p => p?.id);

        if (!resolvedPropertyId) {
          return errorResponse('NO_PROPERTY', 'No property configured. Please contact front desk.');
        }

        const wifiUsername = `voucher-${voucher.code.toLowerCase()}`;
        const validUntil = new Date(now.getTime() + portalSessionTimeoutMin * 60 * 1000);
        const dataLimitMb = voucher.plan?.dataLimit ?? undefined;

        // Use PLAN bandwidth (Mbps → kbps), NOT portal defaults
        const planDnKbps = (voucher.plan?.downloadSpeed || bwDown) * 1000;
        const planUpKbps = (voucher.plan?.uploadSpeed || bwUp) * 1000;

        await provisionOrResumeUser(wifiUsername, now, validUntil, {
          tenantId: voucher.tenantId,
          propertyId: resolvedPropertyId,
          guestId: voucher.guestId ?? undefined,
          bookingId: voucher.bookingId ?? undefined,
          username: wifiUsername,
          password: voucher.code,
          planId: voucher.planId ?? undefined,
          planName: voucher.plan?.name,
          downloadSpeed: planDnKbps * 1000,  // kbps → bits/sec for RADIUS
          uploadSpeed: planUpKbps * 1000,
          sessionTimeoutMinutes: portalSessionTimeoutMin,
          idleTimeoutSeconds: portal?.idleTimeout,
          sessionLimit: voucher.plan?.maxDevices,
          dataLimit: dataLimitMb,
        });

        // Check concurrent session limit
        const maxSessions = voucher.plan?.maxDevices || 1;
        if (await isSessionLimitReached(wifiUsername, maxSessions)) {
          await logAuthAttempt(wifiUsername, 'Access-Reject', request, 'MAX_SESSIONS_REACHED');
          return errorResponse('MAX_SESSIONS_REACHED', 'Maximum concurrent sessions reached. Please disconnect another device first.');
        }

        const radiusResult = await radiusAuth(wifiUsername, voucher.code);
        if (!radiusResult.accepted) {
          await logAuthAttempt(wifiUsername, 'Access-Reject', request, radiusResult.rejectReason || 'AUTH_FAILED');
          return errorResponse(radiusResult.rejectReason || 'AUTH_FAILED', getRejectMessage(radiusResult.rejectReason || 'AUTH_FAILED'));
        }

        // Atomically mark voucher as used (prevents double-use race condition)
        const updatedVoucher = await db.wiFiVoucher.updateMany({
          where: { id: voucher.id, isUsed: false },
          data: { isUsed: true, usedAt: new Date(), status: 'used' },
        });
        if (updatedVoucher.count === 0) {
          return NextResponse.json({ success: false, error: 'Voucher already used' }, { status: 409 });
        }

        await logAuthAttempt(wifiUsername, 'Access-Accept', request, `pool:${pool.poolName}`);
        const sessionId = await createAccountingSession(wifiUsername, request, 'portal', effectiveMac, pool);

        // Activate nftables + TC bandwidth shaping — use PLAN speed in kbps, not portal defaults
        const voucherBw = await resolvePlanBandwidthKbps(
          voucher.planId, wifiUsername,
          portal?.maxBandwidthDown, portal?.maxBandwidthUp,
        );
        await activateUserFirewall({
          username: wifiUsername, clientIp: getClientIpString(request),
          propertyId: resolvedPropertyId, sessionId,
          macAddress: effectiveMac,
          dnKbps: voucherBw.dn,
          upKbps: voucherBw.up,
          dnCeilKbps: voucherBw.dnCeil,
          upCeilKbps: voucherBw.upCeil,
          subnet: pool.subnet,
        });

        // Add per-IP byte counter rules for session engine tracking
        const voucherCounterIp = normalizeIp(getClientIpString(request));
        if (voucherCounterIp && voucherCounterIp !== '0.0.0.0') {
          addUserCounter(voucherCounterIp);
        }

        // ── Save device profile with real browser fingerprint ──
        try {
          const voucherUser = await db.wiFiUser.findUnique({ where: { username: wifiUsername }, select: { id: true, tenantId: true, propertyId: true, guestId: true } });
          if (voucherUser) {
            await upsertDeviceProfileWithFingerprint({ wifiUserId: voucherUser.id, tenantId: voucherUser.tenantId, propertyId: voucherUser.propertyId, guestId: voucherUser.guestId, username: wifiUsername, request, macAddress: effectiveMac, fingerprintHash, storageToken });
            // Save guest info from portal form
            await saveGuestInfoAfterAuth({
              wifiUserId: voucherUser.id,
              guestId: voucherUser.guestId,
              bookingId: voucher.bookingId,
              guestInfo: normalizedGuestInfo,
              marketingConsent: { emailConsent: marketingEmailConsent === 'true', smsConsent: marketingSmsConsent === 'true' },
            });
          }
        } catch { /* best effort */ }

        return successResponse({
          authenticated: true, method: 'voucher', username: wifiUsername,
          sessionTimeout: portalSessionTimeoutMin, bandwidthDown: bwDown, bandwidthUp: bwUp,
          poolName: pool.poolName, message: 'Connected successfully!',
        });
      }

      // ─── Room Number ──────────────────────────────────────
      // Unification: If a PMS-provisioned WiFiUser already exists for this
      // guest/booking, reuse it instead of creating a duplicate room-{number}
      // user. This gives the guest plan-level bandwidth, data limits, and
      // session timeout from their actual WiFi plan (not portal defaults).
      //
      // Lookup priority:
      //   1. WiFiUser where bookingId = match.id  (PMS auto-provisioned for this booking)
      //   2. WiFiUser where guestId = match.primaryGuestId AND status = 'active'
      //   3. Fallback: create room-{number} user (original behavior)
      case 'room_number': {
        if (!roomNumber?.trim()) {
          return errorResponse('MISSING_ROOM', 'Please enter your room number');
        }
        if (!lastName?.trim()) {
          return errorResponse('MISSING_NAME', 'Please enter your last name');
        }

        const bookings = await db.booking.findMany({
          where: { room: { roomNumber: roomNumber.trim().toUpperCase() }, status: 'in_house' },
          include: { primaryGuest: true, room: true, roomType: { select: { wifiPlanId: true } } },
          take: 10,
        });

        const match = bookings.find(
          (b) => b.primaryGuest.lastName.toLowerCase() === lastName.trim().toLowerCase()
        );

        if (!match) {
          await logAuthAttempt(`room-${roomNumber.trim().toLowerCase()}`, 'Access-Reject', request, 'INVALID_CREDENTIALS');
          return errorResponse('ROOM_NOT_FOUND', 'No active guest found for this room number and last name. Please verify and try again.');
        }

        // ── Credentials valid — now resolve plan ──
        // Priority: roomType's WiFi plan → property's default AAA plan
        // This plan determines bandwidth, device limit, data cap, session timeout
        let roomPlanIpPoolId: string | null = null;
        let roomPlanId: string | null = match.roomType?.wifiPlanId ?? null;

        if (roomPlanId) {
          const roomPlan = await db.wiFiPlan.findUnique({
            where: { id: roomPlanId },
            select: { ipPoolId: true },
          });
          roomPlanIpPoolId = roomPlan?.ipPoolId ?? null;
        } else {
          // No plan on room type — fall back to property's default AAA plan
          const aaaConfig = await db.wiFiAAAConfig.findUnique({
            where: { propertyId: match.propertyId },
            select: { defaultPlanId: true },
          });
          if (aaaConfig?.defaultPlanId) {
            roomPlanId = aaaConfig.defaultPlanId;
            const defaultPlan = await db.wiFiPlan.findUnique({
              where: { id: roomPlanId },
              select: { ipPoolId: true },
            });
            roomPlanIpPoolId = defaultPlan?.ipPoolId ?? null;
          }
        }

        const now = new Date();

        // ── Look for existing PMS-provisioned WiFiUser ──
        // Priority 1: Exact booking match (most precise — created at check-in)
        const existingByBooking = await db.wiFiUser.findFirst({
          where: { bookingId: match.id, status: { in: ['active', 'suspended'] } },
          include: { plan: { select: { ipPoolId: true, maxDevices: true, validityMinutes: true, validityDays: true, downloadSpeed: true, uploadSpeed: true, name: true } } },
        });

        // Priority 2: Same guest, active user (covers cases where bookingId wasn't set)
        const existingByGuest = !existingByBooking
          ? await db.wiFiUser.findFirst({
              where: { guestId: match.primaryGuestId, status: 'active', propertyId: match.propertyId },
              include: { plan: { select: { ipPoolId: true, maxDevices: true, validityMinutes: true, validityDays: true, downloadSpeed: true, uploadSpeed: true, name: true } } },
            })
          : null;

        const existingUser = existingByBooking || existingByGuest;
        const reuseExisting = !!existingUser;

        if (reuseExisting) {
          // ── Reuse existing PMS-provisioned user ──
          // This guest was auto-provisioned at check-in — use their plan attrs
          const pmsUser = existingUser!;

          if (pmsUser.status !== 'active') {
            await logAuthAttempt(pmsUser.username, 'Access-Reject', request, 'ACCOUNT_INACTIVE');
            return errorResponse('ACCOUNT_INACTIVE', 'Your WiFi account is not active. Please contact front desk.');
          }

          if (new Date(pmsUser.validUntil) < now) {
            await logAuthAttempt(pmsUser.username, 'Access-Reject', request, 'ACCOUNT_EXPIRED');
            return errorResponse('ACCOUNT_EXPIRED', 'Your WiFi session has expired. Please contact front desk to renew.');
          }

          const allowedPools = resolveAllowedPoolIds(pmsUser.plan?.ipPoolId || roomPlanIpPoolId, pmsUser.ipPoolId, !!(pmsUser.plan?.ipPoolId || roomPlanIpPoolId));
          const pool = await getValidatedPool(request, allowedPools);
          if (!pool) {
            await logAuthAttempt(pmsUser.username, 'Access-Reject', request, `IP_NOT_IN_POOL:${getClientIpString(request)}`);
            return errorResponse('IP_NOT_IN_POOL', 'Your device is not connected to a managed WiFi network. Please connect to the hotel WiFi and try again.', 403);
          }

          // Ensure RADIUS credentials exist (may have been deleted between sessions)
          const existingCheck = await db.radCheck.findFirst({ where: { username: pmsUser.username } });
          if (!existingCheck) {
            try { await wifiUserService.resumeUser(pmsUser.id); } catch { /* best effort */ }
          }

          // Check concurrent session limit
          const pmsMaxSessions = pmsUser.maxSessions || pmsUser.plan?.maxDevices || 1;
          if (await isSessionLimitReached(pmsUser.username, pmsMaxSessions)) {
            await logAuthAttempt(pmsUser.username, 'Access-Reject', request, 'MAX_SESSIONS_REACHED');
            return errorResponse('MAX_SESSIONS_REACHED', 'Maximum concurrent sessions reached. Please disconnect another device first.');
          }

          const radiusResult = await radiusAuth(pmsUser.username, pmsUser.password);
          if (!radiusResult.accepted) {
            await logAuthAttempt(pmsUser.username, 'Access-Reject', request, radiusResult.rejectReason || 'AUTH_FAILED');
            return errorResponse(radiusResult.rejectReason || 'AUTH_FAILED', getRejectMessage(radiusResult.rejectReason || 'AUTH_FAILED'));
          }

          // ── Calculate remaining validity (NEVER reset validUntil) ──
          const planValidityMin = pmsUser.plan?.validityMinutes
            || (pmsUser.plan?.validityDays ? pmsUser.plan.validityDays * 1440 : null)
            || portalSessionTimeoutMin;
          const remainingMs = new Date(pmsUser.validUntil).getTime() - now.getTime();
          const remainingMinutes = Math.max(0, Math.ceil(remainingMs / 60000));
          const remainingSeconds = Math.max(0, Math.floor(remainingMs / 1000));

          // Cap RADIUS Session-Timeout to remaining validity
          const cappedSessionTimeoutSec = Math.min(planValidityMin * 60, remainingSeconds);
          await db.radReply.deleteMany({ where: { username: pmsUser.username, attribute: 'Session-Timeout' } });
          await db.radReply.create({
            data: {
              wifiUserId: pmsUser.id, username: pmsUser.username,
              attribute: 'Session-Timeout', op: ':=',
              value: String(cappedSessionTimeoutSec), isActive: true,
            },
          });

          // Update last activity timestamp
          await db.wiFiUser.update({ where: { id: pmsUser.id }, data: { validFrom: now } });

          // Read bandwidth from radreply (user-level overrides > group-level)
          const pmsRadreply = await db.radReply.findMany({ where: { username: pmsUser.username } });
          const getRadReplyVal = (attr: string): string | undefined =>
            pmsRadreply.find(r => r.attribute === attr)?.value;
          const pmsBwDownBps = getRadReplyVal('WISPr-Bandwidth-Max-Down') || getRadReplyVal('Cryptsk-Bandwidth-Max-Down');
          const pmsBwUpBps = getRadReplyVal('WISPr-Bandwidth-Max-Up') || getRadReplyVal('Cryptsk-Bandwidth-Max-Up');
          const pmsBwDown = pmsBwDownBps ? Math.round(Number(pmsBwDownBps) / 1000000) : (pmsUser.plan?.downloadSpeed || bwDown);
          const pmsBwUp = pmsBwUpBps ? Math.round(Number(pmsBwUpBps) / 1000000) : (pmsUser.plan?.uploadSpeed || bwUp);

          await logAuthAttempt(pmsUser.username, 'Access-Accept', request, `pool:${pool.poolName} reuse:pms plan:${pmsUser.plan?.name || 'none'}`);
          const sessionId = await createAccountingSession(pmsUser.username, request, 'portal', effectiveMac, pool);

          // Activate firewall — resolve from plan in kbps
          const pmsBw = await resolvePlanBandwidthKbps(
            pmsUser.planId, pmsUser.username,
            portal?.maxBandwidthDown, portal?.maxBandwidthUp,
          );
          await activateUserFirewall({
            username: pmsUser.username, clientIp: getClientIpString(request),
            propertyId: match.propertyId, sessionId,
            macAddress: effectiveMac, userId: pmsUser.id,
            dnKbps: pmsBw.dn,
            upKbps: pmsBw.up,
            dnCeilKbps: pmsBw.dnCeil,
            upCeilKbps: pmsBw.upCeil,
            subnet: pool.subnet,
          });

          const pmsCounterIp = normalizeIp(getClientIpString(request));
          if (pmsCounterIp && pmsCounterIp !== '0.0.0.0') {
            addUserCounter(pmsCounterIp);
          }

          // ── Save device profile with real browser fingerprint ──
          await upsertDeviceProfileWithFingerprint({
            wifiUserId: pmsUser.id, tenantId: pmsUser.tenantId,
            propertyId: pmsUser.propertyId, guestId: pmsUser.guestId,
            username: pmsUser.username, request,
            macAddress: effectiveMac, fingerprintHash, storageToken,
          });

          // Save guest info from portal form
          saveGuestInfoAfterAuth({
            wifiUserId: pmsUser.id,
            guestId: pmsUser.guestId,
            bookingId: match.id,
            guestInfo: normalizedGuestInfo,
            marketingConsent: { emailConsent: marketingEmailConsent === 'true', smsConsent: marketingSmsConsent === 'true' },
          }).catch(() => {});

          return successResponse({
            authenticated: true, method: 'room_number', username: pmsUser.username,
            sessionTimeout: planValidityMin, remainingMinutes,
            bandwidthDown: pmsBwDown, bandwidthUp: pmsBwUp,
            poolName: pool.poolName, planName: pmsUser.plan?.name || null,
            message: 'Connected successfully!',
          });
        }

        // ── No PMS user found — fallback: create room-{number} user ──
        console.log(`[Room Auth] No PMS user found for booking ${match.id} / guest ${match.primaryGuestId} — falling back to room-${match.room?.roomNumber?.toLowerCase() || roomNumber.trim().toLowerCase()}`);

        const pool = await getValidatedPool(request, resolveAllowedPoolIds(roomPlanIpPoolId, undefined, !!roomPlanIpPoolId));
        if (!pool) {
          await logAuthAttempt(`room-${roomNumber.trim().toLowerCase()}`, 'Access-Reject', request, `IP_NOT_IN_POOL:${getClientIpString(request)}`);
          return errorResponse('IP_NOT_IN_POOL', 'Your device is not connected to a managed WiFi network. Please connect to the hotel WiFi and try again.', 403);
        }

        const validUntil = new Date(now.getTime() + portalSessionTimeoutMin * 60 * 1000);
        const wifiUsername = `room-${match.room?.roomNumber?.toLowerCase() || roomNumber.trim().toLowerCase()}`;
        const userPassword = `${match.primaryGuest.lastName.toLowerCase()}-${match.id.slice(0, 8)}`;

        // Resolve plan attrs for bandwidth, device limit and data cap
        let roomMaxDevices = 1; // safe default
        let roomDataLimit: number | undefined;
        let roomPlanDnKbps = bwDown * 1000;  // kbps
        let roomPlanUpKbps = bwUp * 1000;    // kbps
        if (roomPlanId) {
          const planAttrs = await db.wiFiPlan.findUnique({
            where: { id: roomPlanId },
            select: { maxDevices: true, dataLimit: true, downloadSpeed: true, uploadSpeed: true },
          });
          if (planAttrs?.maxDevices && planAttrs.maxDevices > 0) {
            roomMaxDevices = planAttrs.maxDevices;
          }
          if (planAttrs?.dataLimit && planAttrs.dataLimit > 0) {
            roomDataLimit = planAttrs.dataLimit;
          }
          if (planAttrs?.downloadSpeed && planAttrs.downloadSpeed > 0) {
            roomPlanDnKbps = planAttrs.downloadSpeed * 1000;  // Mbps → kbps
            roomPlanUpKbps = planAttrs.uploadSpeed * 1000;
          }
        }

        await provisionOrResumeUser(wifiUsername, now, validUntil, {
          tenantId: match.tenantId,
          propertyId: match.propertyId,
          guestId: match.primaryGuestId,
          bookingId: match.id,
          username: wifiUsername,
          password: userPassword,
          planId: roomPlanId ?? undefined,
          downloadSpeed: roomPlanDnKbps * 1000,  // kbps → bits/sec for RADIUS
          uploadSpeed: roomPlanUpKbps * 1000,
          sessionTimeoutMinutes: portalSessionTimeoutMin,
          idleTimeoutSeconds: portal?.idleTimeout,
          sessionLimit: roomMaxDevices,
          dataLimit: roomDataLimit,
        });

        // Check concurrent session limit (from resolved plan, not hardcoded)
        if (await isSessionLimitReached(wifiUsername, roomMaxDevices)) {
          await logAuthAttempt(wifiUsername, 'Access-Reject', request, 'MAX_SESSIONS_REACHED');
          return errorResponse('MAX_SESSIONS_REACHED', 'Maximum concurrent sessions reached. Please disconnect another device first.');
        }

        const radiusResult = await radiusAuth(wifiUsername, userPassword);
        if (!radiusResult.accepted) {
          await logAuthAttempt(wifiUsername, 'Access-Reject', request, radiusResult.rejectReason || 'AUTH_FAILED');
          return errorResponse(radiusResult.rejectReason || 'AUTH_FAILED', getRejectMessage(radiusResult.rejectReason || 'AUTH_FAILED'));
        }

        await logAuthAttempt(wifiUsername, 'Access-Accept', request, `pool:${pool.poolName} fallback:room_user`);
        const sessionId = await createAccountingSession(wifiUsername, request, 'portal', effectiveMac, pool);

        await activateUserFirewall({
          username: wifiUsername, clientIp: getClientIpString(request),
          propertyId: match.propertyId, sessionId,
          macAddress: effectiveMac,
          dnKbps: roomPlanDnKbps,
          upKbps: roomPlanUpKbps,
          subnet: pool.subnet,
        });

        // Add per-IP byte counter rules for session engine tracking
        const roomCounterIp = normalizeIp(getClientIpString(request));
        if (roomCounterIp && roomCounterIp !== '0.0.0.0') {
          addUserCounter(roomCounterIp);
        }

        // ── Save device profile with real browser fingerprint ──
        try {
          const roomUser = await db.wiFiUser.findUnique({ where: { username: wifiUsername }, select: { id: true, tenantId: true, propertyId: true, guestId: true } });
          if (roomUser) {
            await upsertDeviceProfileWithFingerprint({ wifiUserId: roomUser.id, tenantId: roomUser.tenantId, propertyId: roomUser.propertyId, guestId: roomUser.guestId, username: wifiUsername, request, macAddress: effectiveMac, fingerprintHash, storageToken });
            // Save guest info from portal form
            await saveGuestInfoAfterAuth({
              wifiUserId: roomUser.id,
              guestId: roomUser.guestId,
              bookingId: match.id,
              guestInfo: normalizedGuestInfo,
              marketingConsent: { emailConsent: marketingEmailConsent === 'true', smsConsent: marketingSmsConsent === 'true' },
            });
          }
        } catch { /* best effort */ }

        return successResponse({
          authenticated: true, method: 'room_number', username: wifiUsername,
          sessionTimeout: portalSessionTimeoutMin, bandwidthDown: bwDown, bandwidthUp: bwUp,
          poolName: pool.poolName, message: 'Connected successfully!',
        });
      }

      // ─── PMS Credentials ──────────────────────────────────
      case 'pms_credentials': {
        if (!username?.trim()) {
          return errorResponse('MISSING_USERNAME', 'Please enter your username');
        }
        if (!password?.trim()) {
          return errorResponse('MISSING_PASSWORD', 'Please enter your password');
        }

        const wifiUser = await db.wiFiUser.findUnique({
          where: { username: username.trim() },
          include: {
            plan: { select: { ipPoolId: true, maxDevices: true, validityMinutes: true, downloadSpeed: true, uploadSpeed: true } },
            ipPool: { select: { id: true } },
          },
        });

        if (!wifiUser) {
          await logAuthAttempt(username.trim(), 'Access-Reject', request, 'INVALID_CREDENTIALS');
          return errorResponse('INVALID_CREDENTIALS', 'Invalid username or password');
        }

        if (wifiUser.password !== password.trim()) {
          await logAuthAttempt(username.trim(), 'Access-Reject', request, 'INVALID_CREDENTIALS');
          return errorResponse('INVALID_CREDENTIALS', 'Invalid username or password');
        }

        if (wifiUser.status !== 'active') {
          await logAuthAttempt(username.trim(), 'Access-Reject', request, 'ACCOUNT_INACTIVE');
          return errorResponse('ACCOUNT_INACTIVE', 'Your WiFi account is not active. Please contact front desk.');
        }

        const now = new Date();
        if (wifiUser.validUntil < now) {
          await logAuthAttempt(username.trim(), 'Access-Reject', request, 'ACCOUNT_EXPIRED');
          return errorResponse('ACCOUNT_EXPIRED', 'Your WiFi session has expired. Please contact front desk to renew.');
        }

        // ── Credentials valid — now check IP pool ──
        const allowedPools = resolveAllowedPoolIds(wifiUser.plan?.ipPoolId, wifiUser.ipPoolId, !!wifiUser.planId);
        const pool = await getValidatedPool(request, allowedPools);
        if (!pool) {
          await logAuthAttempt(username.trim(), 'Access-Reject', request, `IP_NOT_IN_POOL:${getClientIpString(request)}`);
          return errorResponse('IP_NOT_IN_POOL', 'Your device is not connected to a managed WiFi network. Please connect to the hotel WiFi and try again.', 403);
        }

        // Ensure RADIUS credentials exist
        const existingCheck = await db.radCheck.findFirst({
          where: { username: wifiUser.username },
        });
        if (!existingCheck) {
          try {
            await wifiUserService.resumeUser(wifiUser.id);
          } catch {
            // Best effort
          }
        }

        // Check concurrent session limit
        const pmsMaxSessions = wifiUser.maxSessions || wifiUser.plan?.maxDevices || 1;
        if (await isSessionLimitReached(username.trim(), pmsMaxSessions)) {
          await logAuthAttempt(username.trim(), 'Access-Reject', request, 'MAX_SESSIONS_REACHED');
          return errorResponse('MAX_SESSIONS_REACHED', 'Maximum concurrent sessions reached. Please disconnect another device first.');
        }

        const radiusResult = await radiusAuth(username.trim(), password.trim());
        if (!radiusResult.accepted) {
          await logAuthAttempt(username.trim(), 'Access-Reject', request, radiusResult.rejectReason || 'AUTH_FAILED');
          return errorResponse(radiusResult.rejectReason || 'AUTH_FAILED', getRejectMessage(radiusResult.rejectReason || 'AUTH_FAILED'));
        }

        await logAuthAttempt(username.trim(), 'Access-Accept', request, `pool:${pool.poolName}`);
        const sessionId = await createAccountingSession(username.trim(), request, 'portal', effectiveMac, pool);

        // ── Calculate plan validity for session timeout & display ──
        // validUntil is NOT reset here — it was set ONCE at user creation.
        // This ensures timed packages (e.g. 4-hr) give exactly that much total time.
        const planValidityMin = wifiUser.plan?.validityMinutes
          || (wifiUser.plan?.validityDays ? wifiUser.plan.validityDays * 1440 : null)
          || portalSessionTimeoutMin;

        // Calculate remaining time for display and Session-Timeout capping
        const remainingMs = wifiUser.validUntil
          ? new Date(wifiUser.validUntil).getTime() - now.getTime()
          : planValidityMin * 60 * 1000;
        const remainingMinutes = Math.max(0, Math.ceil(remainingMs / 60000));
        const remainingSeconds = Math.max(0, Math.floor(remainingMs / 1000));

        // ── Set validFrom to now (tracks last activity), but NEVER reset validUntil ──
        await db.wiFiUser.update({
          where: { id: wifiUser.id },
          data: { validFrom: now },
        });

        // ── Cap RADIUS Session-Timeout to remaining validity ──
        // Prevents RADIUS sessions that outlast the actual account validity.
        const cappedSessionTimeoutSec = Math.min(planValidityMin * 60, remainingSeconds);
        await db.radReply.deleteMany({
          where: { username: wifiUser.username, attribute: 'Session-Timeout' },
        });
        await db.radReply.create({
          data: {
            wifiUserId: wifiUser.id,
            username: wifiUser.username,
            attribute: 'Session-Timeout',
            op: ':=',
            value: String(cappedSessionTimeoutSec),
            isActive: true,
          },
        });

        // ── Read user's bandwidth from radreply ──
        const userRadreply = await db.radReply.findMany({
          where: { username: username.trim() },
        });
        const getRadReplyValue = (attr: string): string | undefined =>
          userRadreply.find(r => r.attribute === attr)?.value;

        const bwDownBps = getRadReplyValue('WISPr-Bandwidth-Max-Down')
          || getRadReplyValue('Cryptsk-Bandwidth-Max-Down');
        const bwUpBps = getRadReplyValue('WISPr-Bandwidth-Max-Up')
          || getRadReplyValue('Cryptsk-Bandwidth-Max-Up');
        const userBwDown = bwDownBps ? Math.round(Number(bwDownBps) / 1000000) : bwDown;
        const userBwUp = bwUpBps ? Math.round(Number(bwUpBps) / 1000000) : bwUp;

        await activateUserFirewall({
          username: username.trim(), clientIp: getClientIpString(request),
          propertyId: wifiUser.propertyId, sessionId,
          macAddress: effectiveMac, userId: wifiUser.id,
          dnKbps: bwDownBps ? Math.round(Number(bwDownBps) / 1000) : (wifiUser.plan?.downloadSpeed || bwDown) * 1000,
          upKbps: bwUpBps ? Math.round(Number(bwUpBps) / 1000) : (wifiUser.plan?.uploadSpeed || bwUp) * 1000,
          subnet: pool.subnet,
        });

        // Add per-IP byte counter rules for session engine tracking
        const pmsCounterIp = normalizeIp(getClientIpString(request));
        if (pmsCounterIp && pmsCounterIp !== '0.0.0.0') {
          addUserCounter(pmsCounterIp);
        }

        // ── Save device profile with real browser fingerprint ──
        await upsertDeviceProfileWithFingerprint({
          wifiUserId: wifiUser.id,
          tenantId: wifiUser.tenantId,
          propertyId: wifiUser.propertyId,
          guestId: wifiUser.guestId,
          username: wifiUser.username,
          request,
          macAddress: effectiveMac,
          fingerprintHash,
          storageToken,
        });

          // Save guest info from portal form
          saveGuestInfoAfterAuth({
            wifiUserId: wifiUser.id,
            guestId: wifiUser.guestId,
            bookingId: wifiUser.bookingId,
            guestInfo: normalizedGuestInfo,
            marketingConsent: { emailConsent: marketingEmailConsent === 'true', smsConsent: marketingSmsConsent === 'true' },
          }).catch(() => {});

        return successResponse({
          authenticated: true, method: 'pms_credentials', username: wifiUser.username,
          sessionTimeout: planValidityMin, remainingMinutes, bandwidthDown: userBwDown, bandwidthUp: userBwUp,
          poolName: pool.poolName, message: 'Connected successfully!',
        });
      }

      // ─── SMS OTP ──────────────────────────────────────────
      // Two-step flow:
      //   Step 1 (no otpCode): Validate phone → generate OTP → send via SMS → store in otpStore
      //   Step 2 (with otpCode): Validate OTP → provision RADIUS user → authenticate
      //
      // Security:
      //   - Per-phone rate limiting (5 sends per 15 min)
      //   - Max 5 wrong OTP attempts per OTP
      //   - 5-minute OTP expiry
      //   - Phone number format validation
      case 'sms_otp': {
        if (otpCode) {
          // ── Step 2: Verify OTP (credential check) ──
          if (!phoneNumber?.trim()) {
            return errorResponse('MISSING_PHONE', 'Phone number is required for OTP verification');
          }

          const normalizedPhone = normalizePhoneNumberLocal(phoneNumber.trim());
          const stored = otpStore.get(normalizedPhone);

          if (!stored) {
            return errorResponse('OTP_NOT_FOUND', 'No OTP found. Please request a new one.');
          }

          if (Date.now() > stored.expiresAt) {
            otpStore.delete(normalizedPhone);
            return errorResponse('OTP_EXPIRED', 'Your OTP has expired. Please request a new one.');
          }

          // Track wrong attempts — invalidate after too many tries
          stored.attempts++;
          if (stored.attempts > OTP_MAX_VERIFY_ATTEMPTS) {
            otpStore.delete(normalizedPhone);
            return errorResponse('OTP_MAX_ATTEMPTS', 'Too many incorrect attempts. Please request a new OTP.');
          }

          if (stored.code !== otpCode.trim()) {
            const remaining = OTP_MAX_VERIFY_ATTEMPTS - stored.attempts;
            return errorResponse('OTP_INVALID', `Invalid OTP code. ${remaining} attempt${remaining !== 1 ? 's' : ''} remaining.`);
          }

          // ── OTP verified — check IP pool ──
          const smsPool = await getValidatedPool(request, resolveAllowedPoolIds(portalPlanIpPoolId, undefined, !!portalPlanIpPoolId));
          if (!smsPool) {
            await logAuthAttempt(`sms-${normalizedPhone.replace(/[^a-z0-9]/gi, '')}`, 'Access-Reject', request, `IP_NOT_IN_POOL:${getClientIpString(request)}`);
            return errorResponse('IP_NOT_IN_POOL', 'Your device is not connected to a managed WiFi network. Please connect to the hotel WiFi and try again.', 403);
          }

          otpStore.delete(normalizedPhone);

          const now = new Date();
          const validUntil = new Date(now.getTime() + portalSessionTimeoutMin * 60 * 1000);
          const wifiUsername = `sms-${normalizedPhone.replace(/[^a-z0-9]/gi, '')}`;

          const fallbackPropertyId = portal?.propertyId
            || await (portal?.tenantId
              ? db.property.findFirst({ where: { tenantId: portal.tenantId }, select: { id: true } }).then(p => p?.id)
              : Promise.resolve(undefined));

          if (fallbackPropertyId) {
            // ── Resolve plan for proper bandwidth, device limit + data cap ──
            let smsPlanId: string | null = null;
            let smsMaxDevices = 1;
            let smsDataLimit: number | undefined;
            let smsPlanDnKbps = bwDown * 1000;  // kbps
            let smsPlanUpKbps = bwUp * 1000;    // kbps

            if (portalPlanIpPoolId || fallbackPropertyId) {
              // Try property default AAA plan
              const aaaConfig = await db.wiFiAAAConfig.findUnique({
                where: { propertyId: fallbackPropertyId },
                select: { defaultPlanId: true },
              });
              if (aaaConfig?.defaultPlanId) {
                smsPlanId = aaaConfig.defaultPlanId;
                const planAttrs = await db.wiFiPlan.findUnique({
                  where: { id: smsPlanId },
                  select: { maxDevices: true, dataLimit: true, downloadSpeed: true, uploadSpeed: true },
                });
                if (planAttrs?.maxDevices && planAttrs.maxDevices > 0) {
                  smsMaxDevices = planAttrs.maxDevices;
                }
                if (planAttrs?.dataLimit && planAttrs.dataLimit > 0) {
                  smsDataLimit = planAttrs.dataLimit;
                }
                if (planAttrs?.downloadSpeed && planAttrs.downloadSpeed > 0) {
                  smsPlanDnKbps = planAttrs.downloadSpeed * 1000;  // Mbps → kbps
                  smsPlanUpKbps = planAttrs.uploadSpeed * 1000;
                }
              }
            }

            await provisionOrResumeUser(wifiUsername, now, validUntil, {
              tenantId: portal?.tenantId || '',
              propertyId: fallbackPropertyId,
              username: wifiUsername,
              password: stored.code,
              planId: smsPlanId ?? undefined,
              downloadSpeed: smsPlanDnKbps * 1000,  // kbps → bits/sec for RADIUS
              uploadSpeed: smsPlanUpKbps * 1000,
              sessionTimeoutMinutes: portalSessionTimeoutMin,
              idleTimeoutSeconds: portal?.idleTimeout,
              sessionLimit: smsMaxDevices,
              dataLimit: smsDataLimit,
            });

            // Check concurrent session limit (from resolved plan, not hardcoded)
            if (await isSessionLimitReached(wifiUsername, smsMaxDevices)) {
              await logAuthAttempt(wifiUsername, 'Access-Reject', request, 'MAX_SESSIONS_REACHED');
              return errorResponse('MAX_SESSIONS_REACHED', 'Maximum concurrent sessions reached. Please disconnect another device first.');
            }

            const radiusResult = await radiusAuth(wifiUsername, stored.code);
            if (!radiusResult.accepted) {
              await logAuthAttempt(wifiUsername, 'Access-Reject', request, radiusResult.rejectReason || 'AUTH_FAILED');
              return errorResponse(radiusResult.rejectReason || 'AUTH_FAILED', getRejectMessage(radiusResult.rejectReason || 'AUTH_FAILED'));
            }

            await logAuthAttempt(wifiUsername, 'Access-Accept', request, `pool:${smsPool.poolName}${smsPlanId ? ' plan:aaa' : ''}`);
            const sessionId = await createAccountingSession(wifiUsername, request, 'portal', effectiveMac, smsPool);

            await activateUserFirewall({
              username: wifiUsername, clientIp: getClientIpString(request),
              propertyId: fallbackPropertyId, sessionId,
              macAddress: effectiveMac,
              dnKbps: smsPlanDnKbps,
              upKbps: smsPlanUpKbps,
              subnet: smsPool.subnet,
            });

            // Add per-IP byte counter rules for session engine tracking
            const smsCounterIp = normalizeIp(getClientIpString(request));
            if (smsCounterIp && smsCounterIp !== '0.0.0.0') {
              addUserCounter(smsCounterIp);
            }

            // ── Save device profile for auto-reauth ──
            try {
              const smsUser = await db.wiFiUser.findUnique({
                where: { username: wifiUsername },
                select: { id: true, tenantId: true, propertyId: true, guestId: true },
              });
              if (smsUser) {
                await upsertDeviceProfileWithFingerprint({
                  wifiUserId: smsUser.id, tenantId: smsUser.tenantId,
                  propertyId: smsUser.propertyId, guestId: smsUser.guestId,
                  username: wifiUsername, request,
                  macAddress: effectiveMac, fingerprintHash, storageToken,
                });
                // Save guest info from portal form
                await saveGuestInfoAfterAuth({
                  wifiUserId: smsUser.id,
                  propertyId: fallbackPropertyId,
                  guestInfo: normalizedGuestInfo,
                  marketingConsent: { emailConsent: marketingEmailConsent === 'true', smsConsent: marketingSmsConsent === 'true' },
                });
              }
            } catch { /* best effort */ }
          }

          return successResponse({
            authenticated: true, method: 'sms_otp', username: wifiUsername,
            sessionTimeout: portalSessionTimeoutMin, bandwidthDown: bwDown, bandwidthUp: bwUp,
            poolName: smsPool.poolName, message: 'Connected successfully!',
          });
        } else {
          // ── Step 1: Send OTP ──
          if (!phoneNumber?.trim()) {
            return errorResponse('MISSING_PHONE', 'Please enter your phone number');
          }

          const normalizedPhone = normalizePhoneNumberLocal(phoneNumber.trim());

          // Validate phone number format
          if (!isValidPhoneNumber(phoneNumber.trim())) {
            return errorResponse('INVALID_PHONE', 'Please enter a valid phone number (10-15 digits).');
          }

          // Per-phone rate limiting (prevent OTP bombing)
          const rateCheck = checkOtpRateLimit(normalizedPhone);
          if (!rateCheck.allowed) {
            return errorResponse(
              'OTP_RATE_LIMITED',
              `Too many OTP requests. Please try again in ${rateCheck.retryAfterSec} seconds.`,
              429,
            );
          }

          const code = generateOtp();

          otpStore.set(normalizedPhone, {
            code,
            expiresAt: Date.now() + 5 * 60 * 1000,
            phone: normalizedPhone,
            attempts: 0,
          });

          // ── Actually send the OTP via SMS ──
          let smsSent = false;
          let smsError: string | undefined;

          try {
            const tenantId = portal?.tenantId || '';
            const result = await sendSMSNow({
              to: normalizedPhone,
              message: `Your StaySuite WiFi verification code is: ${code}. Valid for 5 minutes. Do not share this code.`,
              tenantId: tenantId || undefined,
            });
            smsSent = result.success;
            if (!result.success) {
              smsError = result.error;
              console.error(`[SMS OTP] Failed to send to ${normalizedPhone}: ${smsError}`);
            } else {
              console.log(`[SMS OTP] Sent via ${result.provider} to ${normalizedPhone} (messageId: ${result.messageId})`);
            }
          } catch (err) {
            smsError = err instanceof Error ? err.message : 'SMS delivery failed';
            console.error(`[SMS OTP] Exception sending to ${normalizedPhone}:`, smsError);
          }

          // Always return success if OTP was generated (even if SMS delivery failed,
          // the debug OTP is always available in the response — prevents locking out guests
          // when SMS provider has temporary issues)
          //
          // Always log the full OTP for testing / debugging (visible in dev.log)
          console.log(`[SMS OTP] ═══════════════════════════════════════════════════════`);
          console.log(`[SMS OTP] ★ DEBUG OTP for ${normalizedPhone}: ${code}`);
          console.log(`[SMS OTP]   Expires: 5 min | SMS Sent: ${smsSent} | Provider: ${smsSent ? 'ok' : 'N/A'}`);
          console.log(`[SMS OTP] ═══════════════════════════════════════════════════════`);

          return NextResponse.json({
            success: true,
            data: {
              otpSent: smsSent,
              message: smsSent
                ? 'OTP sent to your phone'
                : 'OTP generated but SMS delivery failed. Please try again.',
              // Always expose OTP for testing without SMS gateway
              // In production with a real SMS gateway, this is harmless —
              // the guest receives the OTP via SMS and ignores this field.
              _debugOtp: code,
              _smsSent: smsSent,
              ...(!smsSent && smsError && { _error: smsError }),
            },
          });
        }
      }

      // ─── Open Access ──────────────────────────────────────
      // No credential validation needed — only IP pool check
      case 'open_access': {
        const pool = await getValidatedPool(request, resolveAllowedPoolIds(portalPlanIpPoolId, undefined, !!portalPlanIpPoolId));
        if (!pool) {
          await logAuthAttempt('open-access', 'Access-Reject', request, `IP_NOT_IN_POOL:${getClientIpString(request)}`);
          return errorResponse('IP_NOT_IN_POOL', 'Your device is not connected to a managed WiFi network. Please connect to the hotel WiFi and try again.', 403);
        }

        const now = new Date();
        const validUntil = new Date(now.getTime() + portalSessionTimeoutMin * 60 * 1000);
        let wifiUsername: string | null = null;

        if (portal) {
          const openTimestamp = Date.now();
          wifiUsername = `open-${openTimestamp}`;
          const openPassword = `open-${openTimestamp}`;

          try {
            const resolvedPropertyId = portal.propertyId
              || await db.property.findFirst({ where: { tenantId: portal.tenantId }, select: { id: true } }).then(p => p?.id);

            if (resolvedPropertyId) {
              // Resolve plan bandwidth for open access users (AAA default plan if configured)
              let openPlanDnKbps = bwDown * 1000;  // kbps
              let openPlanUpKbps = bwUp * 1000;    // kbps
              const aaaConfig = await db.wiFiAAAConfig.findUnique({
                where: { propertyId: resolvedPropertyId },
                select: { defaultPlanId: true },
              }).catch(() => null);
              if (aaaConfig?.defaultPlanId) {
                const openPlan = await db.wiFiPlan.findUnique({
                  where: { id: aaaConfig.defaultPlanId },
                  select: { downloadSpeed: true, uploadSpeed: true },
                }).catch(() => null);
                if (openPlan?.downloadSpeed && openPlan.downloadSpeed > 0) {
                  openPlanDnKbps = openPlan.downloadSpeed * 1000;  // Mbps → kbps
                  openPlanUpKbps = openPlan.uploadSpeed * 1000;
                }
              }

              await wifiUserService.provisionUser({
                tenantId: portal.tenantId,
                propertyId: resolvedPropertyId,
                username: wifiUsername,
                password: openPassword,
                validFrom: now,
                validUntil,
                userType: 'guest',
                downloadSpeed: openPlanDnKbps * 1000,  // kbps → bits/sec for RADIUS
                uploadSpeed: openPlanUpKbps * 1000,
                sessionTimeoutMinutes: portalSessionTimeoutMin,
                idleTimeoutSeconds: portal?.idleTimeout,
              });

              // Check concurrent session limit (open access users get default 1)
              if (await isSessionLimitReached(wifiUsername, 1)) {
                await logAuthAttempt(wifiUsername, 'Access-Reject', request, 'MAX_SESSIONS_REACHED');
                return errorResponse('MAX_SESSIONS_REACHED', 'Maximum concurrent sessions reached. Please disconnect another device first.');
              }

              const radiusResult = await radiusAuth(wifiUsername, openPassword);
              if (!radiusResult.accepted) {
                await logAuthAttempt(wifiUsername, 'Access-Reject', request, radiusResult.rejectReason || 'AUTH_FAILED');
                return errorResponse(radiusResult.rejectReason || 'AUTH_FAILED', getRejectMessage(radiusResult.rejectReason || 'AUTH_FAILED'));
              }

              await logAuthAttempt(wifiUsername, 'Access-Accept', request, `pool:${pool.poolName}`);
              const sessionId = await createAccountingSession(wifiUsername, request, 'portal', effectiveMac, pool);

              await activateUserFirewall({
                username: wifiUsername, clientIp: getClientIpString(request),
                propertyId: resolvedPropertyId, sessionId,
                macAddress: effectiveMac,
                dnKbps: openPlanDnKbps,
                upKbps: openPlanUpKbps,
                subnet: pool.subnet,
              });

              // Add per-IP byte counter rules for session engine tracking
              const openCounterIp = normalizeIp(getClientIpString(request));
              if (openCounterIp && openCounterIp !== '0.0.0.0') {
                addUserCounter(openCounterIp);
              }
            }
          } catch {
            // Best effort for open access
          }

          // Save guest info from portal form
          saveGuestInfoAfterAuth({
            wifiUsername,
            propertyId: resolvedPropertyId,
            guestInfo: normalizedGuestInfo,
            marketingConsent: { emailConsent: marketingEmailConsent === 'true', smsConsent: marketingSmsConsent === 'true' },
          }).catch(() => {});
        }

        return successResponse({
          authenticated: true, method: 'open_access', username: wifiUsername,
          sessionTimeout: portalSessionTimeoutMin, bandwidthDown: bwDown, bandwidthUp: bwUp,
          poolName: pool.poolName, message: 'Connected successfully!',
        });
      }

      default: {
        return errorResponse('INVALID_METHOD', 'Unsupported authentication method');
      }
    }
  } catch (error) {
    console.error('[Guest Auth API] Error:', error);
    return errorResponse('INTERNAL_ERROR', 'An unexpected error occurred. Please try again or contact front desk.');
  }
}

// ────────────────────────────────────────────────────────────
// Helpers
// ────────────────────────────────────────────────────────────

function successResponse(data: Record<string, unknown>) {
  return NextResponse.json({ success: true, data });
}

function errorResponse(code: string, message: string, status = 400) {
  return NextResponse.json(
    { success: false, error: { code, message } },
    { status }
  );
}

