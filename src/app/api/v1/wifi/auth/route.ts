/**
 * Save guest information collected from the captive portal form.
 * Called AFTER successful authentication in all flows.
 *
 * Strategy:
 *  1. If wifiUserId is known, look up the WiFiUser → get guestId/propertyId/tenantId
 *  2. If guestId exists, UPDATE the existing Guest record (enrich profile)
 *  3. If no guestId, CREATE a new Guest record (for SMS OTP / voucher / open_access guests)
 *  4. Link the new Guest to the WiFiUser via guestId
 *  5. Save passport as idNumber/idType on the Guest record
 *  6. If bookingId provided and no booking linked, try to link
 *  7. Update marketing consent flags (emailOptIn / smsOptIn)
 *
 * All operations are best-effort — failures are logged but don't block auth.
 */
async function saveGuestInfoAfterAuth(params: {
  wifiUserId?: string | null;
  wifiUsername?: string | null;
  tenantId?: string;
  propertyId?: string;
  guestId?: string | null;
  bookingId?: string | null;
  guestInfo?: GuestInfoPayload;
  marketingConsent?: MarketingConsent;
}): Promise<void> {
  const { wifiUserId, wifiUsername, tenantId, propertyId, guestId: existingGuestId, bookingId: existingBookingId, guestInfo, marketingConsent } = params;

  if (!guestInfo && !marketingConsent) return;

  try {
    // Resolve tenantId/propertyId from WiFiUser if not directly available
    let resolvedTenantId = tenantId;
    let resolvedPropertyId = propertyId;
    let resolvedGuestId = existingGuestId;
    let resolvedBookingId = existingBookingId;
    let resolvedWifiUserId = wifiUserId;

    // Look up WiFiUser by ID or by username
    if (!resolvedWifiUserId && wifiUsername) {
      const userByUname = await db.wiFiUser.findUnique({
        where: { username: wifiUsername },
        select: { id: true, tenantId: true, propertyId: true, guestId: true, bookingId: true },
      });
      if (userByUname) {
        resolvedWifiUserId = userByUname.id;
        resolvedTenantId = resolvedTenantId || userByUname.tenantId;
        resolvedPropertyId = resolvedPropertyId || userByUname.propertyId;
        resolvedGuestId = resolvedGuestId || userByUname.guestId;
        resolvedBookingId = resolvedBookingId || userByUname.bookingId;
      }
    }

    if (resolvedWifiUserId && (!resolvedTenantId || !resolvedPropertyId || !resolvedGuestId)) {
      const wifiUser = await db.wiFiUser.findUnique({
        where: { id: resolvedWifiUserId },
        select: { tenantId: true, propertyId: true, guestId: true, bookingId: true },
      });
      if (wifiUser) {
        resolvedTenantId = resolvedTenantId || wifiUser.tenantId;
        resolvedPropertyId = resolvedPropertyId || wifiUser.propertyId;
        resolvedGuestId = resolvedGuestId || wifiUser.guestId;
        resolvedBookingId = resolvedBookingId || wifiUser.bookingId;
      }
    }

    if (!resolvedTenantId || !resolvedPropertyId) {
      console.warn('[GuestInfo] Skipped — no tenantId/propertyId resolved');
      return;
    }

    const updates: Record<string, unknown> = {};
    const createData: Record<string, unknown> = { tenantId: resolvedTenantId, firstName: '', lastName: '' };

    if (guestInfo) {
      if (guestInfo.firstName?.trim()) {
        updates.firstName = guestInfo.firstName.trim();
        createData.firstName = guestInfo.firstName.trim();
      }
      if (guestInfo.lastName?.trim()) {
        updates.lastName = guestInfo.lastName.trim();
        createData.lastName = guestInfo.lastName.trim();
      }
      if (guestInfo.email?.trim()) {
        updates.email = guestInfo.email.trim().toLowerCase();
        createData.email = guestInfo.email.trim().toLowerCase();
      }
      if (guestInfo.phone?.trim()) {
        updates.phone = guestInfo.phone.trim();
        createData.phone = guestInfo.phone.trim();
      }
      if (guestInfo.passport?.trim()) {
        // Save passport/ID as idType + idNumber on Guest
        updates.idType = 'passport';
        updates.idNumber = guestInfo.passport.trim();
        createData.idType = 'passport';
        createData.idNumber = guestInfo.passport.trim();
      }
    }

    if (marketingConsent) {
      // Explicitly set both true and false — GDPR requires consent withdrawal to work
      updates.emailOptIn = !!marketingConsent.emailConsent;
      createData.emailOptIn = !!marketingConsent.emailConsent;
      updates.smsOptIn = !!marketingConsent.smsConsent;
      createData.smsOptIn = !!marketingConsent.smsConsent;
    }

    // Skip if nothing to save
    if (Object.keys(updates).length === 0 && !resolvedGuestId) return;

    if (resolvedGuestId) {
      // Update existing guest record
      await db.guest.update({
        where: { id: resolvedGuestId },
        data: updates,
      });
      console.log(`[GuestInfo] Updated guest ${resolvedGuestId} with ${Object.keys(updates).join(', ')}`);
    } else {
      // Create new guest for anonymous auth flows (SMS OTP, voucher, open_access)
      const newGuest = await db.guest.create({
        data: createData,
      });
      resolvedGuestId = newGuest.id;
      console.log(`[GuestInfo] Created guest ${resolvedGuestId} (${createData.firstName} ${createData.lastName})`);

      // Link guest to WiFiUser
      if (resolvedWifiUserId) {
        await db.wiFiUser.update({
          where: { id: resolvedWifiUserId },
          data: { guestId: resolvedGuestId },
        });
      }
    }

    // If guestInfo.bookingId provided and no booking linked to WiFiUser, try to link
    if (guestInfo?.bookingId?.trim() && resolvedWifiUserId) {
      const bookingRef = guestInfo.bookingId.trim();
      // Build OR conditions — only include { id: bookingRef } if it looks like a UUID
      // (confirmation codes like "RS-2024-004" are not UUIDs and would cause Prisma cast errors)
      const uuidRegex = /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i;
      const orConditions: any[] = [
        { confirmationCode: bookingRef },
      ];
      if (uuidRegex.test(bookingRef)) {
        orConditions.push({ id: bookingRef });
      }
      orConditions.push({ externalRef: bookingRef });

      // Try matching by confirmationCode, then by ID (if UUID), then by externalRef
      const booking = await db.booking.findFirst({
        where: {
          OR: orConditions,
          propertyId: resolvedPropertyId,
        },
        select: { id: true },
      });
      if (booking) {
        await db.wiFiUser.update({
          where: { id: resolvedWifiUserId },
          data: { bookingId: booking.id },
        });
        console.log(`[GuestInfo] Linked booking ${booking.id} to WiFiUser ${resolvedWifiUserId}`);
      } else {
        console.warn(`[GuestInfo] Booking not found for ref: ${bookingRef}`);
      }
    }
  } catch (err) {
    // Non-fatal — guest info persistence should never block authentication
    console.error('[GuestInfo] Failed to save guest info:', err instanceof Error ? err.message : err);
  }
}

/**
 * Validate client IP and check it belongs to an allocated pool.
 * Called AFTER credential validation, BEFORE RADIUS auth.
 * Returns the matched pool or null.
 *
 * @param request - Next.js request object
 * @param allowedPoolIds - Optional list of pool IDs the user is allowed to use.
 *   Derived from WiFiPlan.ipPoolId or WiFiUser.ipPoolId.
 *   If provided and non-empty, the client IP must belong to one of these pools.
 *   If null/empty, any captive portal pool is allowed (backward compatible).
 */
async function getValidatedPool(request: NextRequest, allowedPoolIds?: string[] | 'ANY' | null): Promise<MatchedPool | null> {
  const rawIp = extractClientIp(request);
  const clientIp = normalizeIp(rawIp);

  // clientIp is null when normalizeIp rejects loopback (::1, 127.0.0.1) or non-IPv4
  if (!clientIp) {
    // Cannot determine real client IP — happens when:
    //   1. Dev/sandbox: no reverse proxy forwarding X-Forwarded-For
    //   2. Misconfigured proxy: not setting X-Real-IP / X-Forwarded-For
    //   3. Direct access: bypassing the network (security concern in production)
    //
    // In PRODUCTION: always REJECT — a real captive portal client must come
    // through the managed network (DHCP → proxy → portal), so a missing IP
    // means something is wrong (proxy misconfig, direct access attempt, etc.)
    //
    // In DEVELOPMENT: allow with a sandbox pool so devs can test auth flows
    // without needing a full network stack.
    if (process.env.NODE_ENV === 'production') {
      console.warn(`[Guest Auth] IP pool check REJECTED: client IP is ${rawIp || 'null'} (normalized to null) — in production, all auth requires a valid managed network IP`);
      return null; // Will trigger IP_NOT_IN_POOL error
    }
    console.warn(`[Guest Auth] IP pool check: client IP is ${rawIp || 'null'} (dev/sandbox env) — allowing with sandbox pool`);
    return {
      poolId: 'sandbox',
      poolName: 'Sandbox',
      subnet: null,
      gateway: null,
      captivePortal: true,
      isDefault: true,
    };
  }

  // No restriction (no plan) → still validate IP is in at least one managed pool.
  // Treat same as 'ANY': check all enabled pools, reject if IP not found in any.
  if (allowedPoolIds === null) {
    allowedPoolIds = 'ANY';
  }

  const pool = await validateClientIpInPool(clientIp, allowedPoolIds);
  if (!pool) {
    if (Array.isArray(allowedPoolIds)) {
      console.warn(`[Guest Auth] IP pool check REJECTED: ${clientIp} is not in any allowed pool (allowed: [${allowedPoolIds.join(', ')}])`);
    } else {
      console.warn(`[Guest Auth] IP pool check REJECTED: ${clientIp} is not in any allocated IP pool`);
    }
    return null;
  }

  console.log(`[Guest Auth] IP pool check PASSED: ${clientIp} → pool "${pool.poolName}" (${pool.subnet || 'no subnet'})${Array.isArray(allowedPoolIds) ? ' [plan-restricted]' : ''}`);
  return pool;
}

/**
 * Provision a RADIUS user, or resume an existing one if provisioning fails.
 * Reduces code duplication across auth methods.
 */
async function provisionOrResumeUser(
  wifiUsername: string,
  now: Date,
  validUntil: Date,
  params: {
    tenantId: string;
    propertyId: string;
    guestId?: string;
    bookingId?: string;
    username: string;
    password: string;
    planId?: string;
    planName?: string;
    downloadSpeed: number;
    uploadSpeed: number;
    sessionTimeoutMinutes: number;
    idleTimeoutSeconds?: number;
    sessionLimit?: number;
    dataLimit?: number;
  }
) {
  let provisionErr: Error | null = null;
  try {
    await wifiUserService.provisionUser({ ...params, validFrom: now, validUntil });
  } catch (err) {
    provisionErr = err instanceof Error ? err : new Error(String(err));
    console.error('[Guest Auth] RADIUS provisioning failed:', provisionErr);
    // Attempt recovery: reactivate existing user if one exists
    try {
      const existingUser = await db.wiFiUser.findUnique({ where: { username: wifiUsername } });
      if (existingUser) {
        await db.wiFiUser.update({
          where: { id: existingUser.id },
          data: {
            status: 'active',
            validFrom: now,
            validUntil,
            radiusSynced: true,
            radiusSyncedAt: now,
          },
        });
        console.log('[Guest Auth] Recovered by reactivating existing user:', wifiUsername);
        provisionErr = null; // Recovery succeeded
      }
    } catch (recoveryErr) {
      console.error('[Guest Auth] Recovery also failed:', recoveryErr);
    }
  }
  // Re-throw if both provisioning and recovery failed
  if (provisionErr) {
    throw provisionErr;
  }
}

/**
 * Write an auth log to RadPostAuth table.
 * This feeds the v_auth_logs view → Auth Logs dashboard tab.
 *
 * IMPORTANT: We populate BOTH clientipaddress AND "nasIpAddress" columns.
 * - clientipaddress: the real client IP (from HTTP headers) — used by v_auth_logs
 *   to show "Source IP" in reject messages and the source_ip_address column.
 * - "nasIpAddress": also set to the client IP so the view's nas_ip_address column
 *   is populated even for application-level rejects (not just RADIUS rejects).
 * - pass: stores the rejection reason code (e.g., IP_NOT_IN_POOL:1.2.3.4,
 *   INVALID_CREDENTIALS, ACCOUNT_EXPIRED) so v_auth_logs can build descriptive
 *   reply_message strings.
 */
/**
 * Convert a rejection reason code (stored in radpostauth.pass) to a
 * human-readable message for the replyMessage column.
 */
function getRejectMessageFromCode(code: string): string | null {
  if (code.startsWith('IP_NOT_IN_POOL:')) return `IP not in managed pool: ${code.replace('IP_NOT_IN_POOL:', '')}`;
  if (code === 'IP_NOT_DETERMINED') return 'Could not determine client IP';
  if (code.startsWith('MAX_SESSION')) return 'Max concurrent sessions reached';
  if (code === 'RADIUS_UNREACHABLE') return 'RADIUS server unreachable';
  if (code.startsWith('ACCOUNT_')) return code.replace(/_/g, ' ').toLowerCase();
  if (code.startsWith('INVALID_') || code.startsWith('MISSING_') || code.startsWith('VOUCHER_') || code.startsWith('AUTH_')) return code.replace(/_/g, ' ').toLowerCase();
  return null;
}

async function logAuthAttempt(
  username: string,
  reply: string,
  request: NextRequest,
  extraInfo?: string,
  macAddress?: string,
) {
  try {
    const clientIp = getClientIp(request) || '';

    // Record failed auth attempt for rate limiting — only for credential-related
    // rejections (OTP_INVALID, INVALID_CREDENTIALS, AUTH_FAILED). Policy rejections
    // (IP_NOT_IN_POOL, MAX_SESSIONS, ACCOUNT_EXPIRED) should NOT trigger rate limiting.
    if (reply === 'Access-Reject' && extraInfo && /^(OTP_|INVALID_|AUTH_FAILED|CREDENTIAL)/.test(extraInfo)) {
      recordFailedAttempt(extractClientIp(request) || 'unknown');
    }

    // Resolve MAC: use provided MAC, then fall back to DeviceProfile lookup
    let effectiveMac = macAddress || null;
    if (!effectiveMac) {
      try {
        const wifiUser = await db.wiFiUser.findUnique({ where: { username }, select: { id: true } });
        if (wifiUser) {
          const dp = await db.deviceProfile.findFirst({
            where: { wifiUserId: wifiUser.id, isActive: true, macAddress: { not: null } },
            select: { macAddress: true },
            orderBy: { lastSeenAt: 'desc' },
          });
          if (dp?.macAddress) effectiveMac = dp.macAddress;
        }
      } catch { /* non-fatal */ }
    }

    // clientipaddress = real client IP (from HTTP headers)
    // "nasIpAddress" = 127.0.0.1 for captive portal (the app itself IS the NAS)
    await db.$executeRawUnsafe(
      `INSERT INTO radpostauth (username, pass, reply, authdate, clientipaddress, "nasIpAddress", callingstationid, "replyMessage")
       VALUES ($1, $2, $3, NOW(), $4, '127.0.0.1', $5, $6)`,
      username,
      extraInfo || '',
      reply,
      clientIp,
      effectiveMac,
      extraInfo ? getRejectMessageFromCode(extraInfo) : null,
    );
  } catch (err) {
    // Non-fatal — auth logging failure should not block authentication
    console.error('[Guest Auth] Failed to write auth log:', err);
  }
}

/**
 * Create an accounting session in RadAcct table.
 * This feeds the v_active_sessions view → Active Users dashboard tab.
 *
 * The session is marked as 'start' with no stop time (acctstoptime = NULL),
 * which is how FreeRADIUS represents an active session.
 *
 * @param loginType - 'portal' for manual login, 'auto_reauth' for silent re-auth
 */
async function createAccountingSession(
  username: string,
  request: NextRequest,
  loginType: string = 'portal',
  macAddress?: string,
  pool?: MatchedPool | null,
  propertyId?: string
): Promise<string> {
  try {
    const clientIp = getClientIp(request) || '0.0.0.0';

    const now = new Date();
    const acctSessionId = `${Date.now()}-${randomUUID().slice(0, 8)}`;
    const acctUniqueId = randomUUID();

    // Normalize MAC address format: strip separators, uppercase
    const normalizedMac = macAddress
      ? macAddress.replace(/[:\-\.\s]/g, '').toUpperCase()
      : null;
    // Reformat as AA:BB:CC:DD:EE:FF for RADIUS compatibility
    let formattedMac = normalizedMac && normalizedMac.length === 12
      ? normalizedMac.match(/.{2}/g)?.join(':') || null
      : null;

    // Fallback: if no MAC from request, look up DeviceProfile for this user
    if (!formattedMac) {
      try {
        const wifiUser = await db.wiFiUser.findUnique({ where: { username }, select: { id: true } });
        if (wifiUser) {
          const dp = await db.deviceProfile.findFirst({
            where: { wifiUserId: wifiUser.id, isActive: true, macAddress: { not: null } },
            select: { macAddress: true },
            orderBy: { lastSeenAt: 'desc' },
          });
          if (dp?.macAddress) formattedMac = dp.macAddress;
        }
      } catch { /* non-fatal */ }
    }

    // Build connect-info with pool details for audit trail
    const connectInfoStart = pool
      ? `pool_id=${pool.poolId}|pool_name=${pool.poolName}|subnet=${pool.subnet || 'none'}|gateway=${pool.gateway || 'none'}`
      : '';

    // Get local NAS identity from RadiusNAS config (replaces hardcoded values)
    const localNas = propertyId ? await getLocalNasConfig(propertyId) : { calledStationId: '00:00:00:00:00:01', nasIpAddress: '127.0.0.1', nasIdentifier: 'Cryptsk-Gateway' };

    await db.$executeRawUnsafe(
      `INSERT INTO radacct (
         acctuniqueid, acctsessionid, username,
         nasipaddress, nasporttype, acctstarttime, acctupdatetime,
         acctauthentic, framedipaddress, acctstatus,
         acctinputoctets, acctoutputoctets, acctsessiontime,
         calledstationid, callingstationid, nasidentifier,
         "loginType", connectinfo_start, createdat, updatedat
       ) VALUES (
         $1, $2, $3,
         $4, 'Wireless-802.11', $5, $5,
         'PAP', $6, 'start',
         0, 0, 0,
         $10, $8, $11,
         $7, $9, NOW(), NOW()
       )`,
      acctUniqueId,
      acctSessionId,
      username,
      localNas.nasIpAddress,
      now,
      clientIp,
      loginType,
      formattedMac,
      connectInfoStart,
      localNas.calledStationId,
      localNas.nasIdentifier || 'cryptsk-gateway',
    );

    // Return the session ID so it can be passed to the login script
    return acctSessionId;
  } catch (err) {
    // Non-fatal — accounting failure should not block authentication
    console.error('[Guest Auth] Failed to create accounting session:', err);
    return null;
  }
}
